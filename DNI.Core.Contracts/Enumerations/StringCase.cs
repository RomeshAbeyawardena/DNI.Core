namespace DNI.Core.Contracts.Enumerations
{
    /// <summary>
    /// Specifies a set of string case conversion options
    /// </summary>
    public enum StringCase
    {
        /// <summary>
        /// No conversion
        /// </summary>
        None = 0,
        /// <summary>
        /// Convert to upper case
        /// </summary>
        Upper = 1,
        /// <summary>
        /// Convert to lower case
        /// </summary>
        Lower = 2
    }
}
